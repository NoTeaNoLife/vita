package com.baldyoung.vita.common.pojo.dto.ProductAttribute;

public class ProductAttributeDto {

    private Integer productAttributeId;

    private String productAttributeName;

    private String productAttributeColor;

    public Integer getProductAttributeId() {
        return productAttributeId;
    }

    public void setProductAttributeId(Integer productAttributeId) {
        this.productAttributeId = productAttributeId;
    }

    public String getProductAttributeName() {
        return productAttributeName;
    }

    public void setProductAttributeName(String productAttributeName) {
        this.productAttributeName = productAttributeName;
    }

    public String getProductAttributeColor() {
        return productAttributeColor;
    }

    public void setProductAttributeColor(String productAttributeColor) {
        this.productAttributeColor = productAttributeColor;
    }
}
